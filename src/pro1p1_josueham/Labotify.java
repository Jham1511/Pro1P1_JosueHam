    public String ListarCanciones() {
        String lista = "";
        for (int i = 1; i <= canciones.size(); i++) {
            lista += i + ". " + canciones.get(i - 1).getNombre() + " - " + canciones.get(i - 1).getArtista() + "\n";
        }
        return lista;
    }


