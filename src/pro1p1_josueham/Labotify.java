    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        String mensaje = ListarCanciones() + "\nIngrese el indice de la cancion que desea eliminar: ";
        String StrIndice = JOptionPane.showInputDialog(null, mensaje);
        int indice = (Integer.parseInt(StrIndice) - 1);
        if (JOptionPane.YES_OPTION == JOptionPane.showInternalConfirmDialog(null, "Seguro que desea eliminar la cancion: "
                + canciones.get(indice).getNombre()
                + " - " + canciones.get(indice).getArtista())) {
            if (indice >= 0 && indice < canciones.size()) {
                canciones.remove(indice);
                JOptionPane.showMessageDialog(null, "Cancion eliminada exitosamente");
            } else {
                JOptionPane.showMessageDialog(null, "La posicion no esta dentro del rango");
            }//fin else
        } 


    }//GEN-LAST:event_BtnEliminarActionPerformed

    private void BtnInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnInfoActionPerformed
        String mensaje = ListarCanciones() + "\nIngrese el indice de la cancion que desea: ";
        String StrIndice = JOptionPane.showInputDialog(null, mensaje);
        int indice = (Integer.parseInt(StrIndice) - 1);
        if (indice >= 0 && indice < canciones.size()) {
            JOptionPane.showMessageDialog(null, canciones.get(indice).toString());
        } else {
            JOptionPane.showMessageDialog(null, "La posicion no esta dentro del rango");
        }//fin else
    }//GEN-LAST:event_BtnInfoActionPerformed

    private void BtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnModificarActionPerformed
        String mensaje = ListarCanciones() + "\nIngrese el indice de la cancion que desea: ";
        String StrIndice = JOptionPane.showInputDialog(null, mensaje);
        indice = (Integer.parseInt(StrIndice) - 1);
        if (indice >= 0 && indice < canciones.size()) {
            Cancion selected = canciones.get(indice);
            TxtNombre.setText(selected.getNombre());
            TxtAlbum.setText(selected.getAlbum());
            TxtArtista.setText(selected.getArtista());
            TxtAnio.setText("" + selected.getAnio());
            TxtDuracion.setText("" + selected.getDuracion());
            FrameModificar.setSize(394, 560);
            FrameModificar.setLocationRelativeTo(null);
            FrameModificar.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "La posicion no esta dentro del rango");
        }//fin else


    }//GEN-LAST:event_BtnModificarActionPerformed

    private void BtnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnGuardarCambiosActionPerformed

        String NueNombre = TxtNombre.getText();
        canciones.get(indice).setNombre(NueNombre);
        String NueAlbum = TxtAlbum.getText();
        canciones.get(indice).setAlbum(NueAlbum);
        String NueArtista = TxtArtista.getText();
        canciones.get(indice).setArtista(NueArtista);
        int NueAnio = Integer.parseInt(TxtAnio.getText());
        canciones.get(indice).setAnio(NueAnio);
        int NueDuracion = Integer.parseInt(TxtDuracion.getText());
        canciones.get(indice).setDuracion(NueDuracion);

        BtnGuardarCambios.setBackground(Color.red);
        BtnGuardarCambios.setForeground(Color.white);
        JOptionPane.showMessageDialog(null, "Cambios realizados con exito");
        FrameModificar.dispose();
    }//GEN-LAST:event_BtnGuardarCambiosActionPerformed

