    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        String nombre = JOptionPane.showInputDialog(this, "Ingrese el nombre de la cancion: ");
        String album = JOptionPane.showInputDialog(this, "Ingrese el nombre del album: ");
        String artista = JOptionPane.showInputDialog(this, "Ingrese el nombre del artista: ");
        String StrAnio = JOptionPane.showInputDialog(this, "Ingrese el año de lanzamiento: ");
        int anio = Integer.parseInt(StrAnio);
        String StrDuracion = JOptionPane.showInputDialog(this, "Ingrese la duracion de la cancion en segundos: ");
        int duracion = Integer.parseInt(StrDuracion);

        canciones.add(new Cancion(nombre, album, artista, anio, duracion));
        JOptionPane.showMessageDialog(null, "Cancion agregada exitosamente");
    }//GEN-LAST:event_BtnAgregarActionPerformed

